export const getQuestionsByTopic = (topic: string, count: number = 10): Question[] => {
  const topicQuestions = questions.filter(q => q.topic === topic);
  const shuffled = [...topicQuestions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, topicQuestions.length));
};

export const getRandomQuestions = (count: number = 5): Question[] => {
  const shuffled = [...questions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

export const getTopicStats = () => {
  const topics = [...new Set(questions.map(q => q.topic))];
  return topics.map(topic => ({
    topic,
    count: questions.filter(q => q.topic === topic).length,
    easy: questions.filter(q => q.topic === topic && q.difficulty === "easy").length,
    medium: questions.filter(q => q.topic === topic && q.difficulty === "medium").length,
    hard: questions.filter(q => q.topic === topic && q.difficulty === "hard").length,
  }));
};
